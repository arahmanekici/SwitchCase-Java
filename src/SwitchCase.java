public class SwitchCase {
    public static void main(String[] args) {

        int day= 8;
        switch(day) {
            case 1:
                System.out.println("Pzt");
                break;
            case 2:
                System.out.println("Salı");
                break;
            case 3:
                System.out.println("Çrş");
                break;
            default:
                System.out.println("Pzt Salı Çrş Değildir");


        }
    }
}
